drop table if exists DOWJONES;

create table DOWJONES(
  ID int not null AUTO_INCREMENT,
  QUARTER int not null,
  STOCK varchar(5) not null,
  DATE DATE not null,
  OPEN NUMBER(20, 8) not null,
  HIGH NUMBER(20, 8),
  LOW NUMBER(20, 8),
  CLOSE NUMBER(20, 8),
  VOLUME BIGINT,
  PERCENT_CHANGE_PRICE NUMBER(20, 8),
  PERCENT_CHANGE_VOLUME_OVER_LAST_WEEK NUMBER(20, 8),
  PREVIOUS_WEEKS_VOLUME NUMBER(20, 8),
  NEXT_WEEKS_OPEN NUMBER(20, 8),
  NEXT_WEEKS_CLOSE NUMBER(20, 8),
  PERCENT_CHANGE_NEXT_WEEKS_PRICE NUMBER(20, 8),
  DAYS_TO_NEXT_DIVIDEND NUMBER(20, 8),
  PERCENT_RETURN_NEXT_DIVIDEND NUMBER(20, 8),
  PRIMARY KEY ( ID )
);
